// 테두리 그리기
void boundry(void) {
	int i, j;
	for (i = 0; i < 44; i++) {
		for (j = 0; j < 24; j++) {
			if (map[i][j] == 1) {
				printf(" * ");
			}
			if (map[i][j] == 0) {
				printf("   ");
			}
			if (map[i][j] == 2) {
				printf("<o>");
			}
		}
		puts(" ");
	}
}

//비행기 움직이기
void control(void) {
	int chr;
	gotoxy(x,y); // 커서 x,y에 두어서 테두리 그릴 처음 부분 커서 위치 설정
	boundry();
	int i = 24; int j = 11;  // 처음 비행기 있는 배열값 설정

	while (1) {
		chr = _getch();

		if (chr == 0 || chr == 0xe0) {
			chr = _getch();

			// 움직임에 따른 배열값 바꿔주기
			if (chr == 72) { //상
				i = i - 1;
				map[i+1][j] = 0;
				if (map[i+1][j] == 0) {
					gotoxy(3 * j, i + 1);
					printf("   ");
				}
				map[i][j] = 2;
				if (map[i][j] == 2) {
					gotoxy(3 * j, i);
					printf("<o>");
				}
			}
			else if (chr == 80) { //하
				i = i + 1;
				map[i - 1][j] = 0;
				if (map[i-1][j] == 0) {
					gotoxy(3 * j, i - 1);
					printf("   ");
				}
				map[i][j] = 2;
				if (map[i][j] == 2) {
					gotoxy(3*j, i);
					printf("<o>");
				}
			}
			else if (chr == 75) { //좌
				j = j - 1;
				map[i][j + 1] = 0;
				map[i][j] = 2;
				if (map[i][j + 1] == 0) {
					gotoxy(3 * (j + 1), i);
					printf("   ");
				}
				if (map[i][j] == 2) {
					gotoxy(3 * j, i);
					printf("<o>");
				}
			}
			else if (chr == 77) { //우
				j = j + 1;
				map[i][j - 1] = 0;
				map[i][j] = 2; 
				if (map[i][j - 1] == 0) {
					gotoxy(3*(j-1), i);
					printf("   ");
				}
				if (map[i][j] == 2) {
					gotoxy(3*j, i);
					printf("<o>");
				}
			}
		}
	}
}


int main(void) {
	// 창 크기 설정
	system("mode con cols=100 lines=60");

	// 커서 비표시
	CONSOLE_CURSOR_INFO Curinfo;
	Curinfo.dwSize = 100; //커서의 두께
	Curinfo.bVisible = 1; //커서 노출 여부 변수
	SetConsoleCursorInfo(GetStdHandle(STD_OUTPUT_HANDLE), &Curinfo);

	boolean start = 1; // 시동

	while (start) {
		control();
	}

	return 0;
}